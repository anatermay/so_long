#include "libft.h"

int	ft_isalpha(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
		return (1);
	return (0);
}
/*The isalpha() function returns zero if the character tests false and
     returns non-zero if the character tests true.*/